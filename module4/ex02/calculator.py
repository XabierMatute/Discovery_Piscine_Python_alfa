#!/usr/bin/python3
print("Enter the first number:")
n1 = int(input())
print("Enter the second number:")
n2 = int(input())
number = n1 + n2
print(f"{n1} + {n2} = {number}")
number = n1 - n2
print(f"{n1} - {n2} = {number}")
number = int(n1 / n2)
print(f"{n1} / {n2} = {number}")
number = n1 * n2
print(f"{n1} x {n2} = {number}")



